def hora_video(hora_inicial_str: str, video_id: int, duracion_min=24) -> str:
    """
    Calcula la hora de inicio de un video dado un ID y una hora inicial.

    hora_inicial_str: str  -> formato "HH:MM"
    video_id: int          -> id del video (comienza desde 0 o 1)
    duracion_min: int      -> duración del video en minutos
    """
    # Convertir string a objeto datetime
    hora_inicial = datetime.strptime(hora_inicial_str, "%H:%M")

    #La hora inicial esta corrida 24 minutos antes!!
    hora_inicial = hora_inicial - timedelta(minutes = 24)
    # Calcular desplazamiento

    desplazamiento = timedelta(minutes = (video_id - 1) * duracion_min)

    # Sumar desplazamiento
    hora_final = hora_inicial + desplazamiento

    # Devolver como string
    return hora_final.strftime("%H:%M")

