def check_crossing_and_count(track_id, prev_cy, curr_cy, counting_line_y, counted_ids, vehicle_counts, vehicle_type):
    """
    Verifica si un vehículo cruzó la línea de conteo y actualiza los contadores.

    Args:
        track_id (int): ID del objeto rastreado.
        prev_cy (int): Coordenada Y central en el frame anterior.
        curr_cy (int): Coordenada Y central en el frame actual.
        counting_line_y (int): Coordenada Y de la línea de conteo.
        counted_ids (set): Conjunto de IDs ya contados para evitar el doble conteo.
        vehicle_counts (defaultdict): Diccionario de conteos por tipo de vehículo.
        vehicle_type (str): Tipo de vehículo (ej. 'car', 'truck').

    Returns:
        bool: True si el vehículo fue contado, False en caso contrario.
    """
    if track_id not in counted_ids:
        # Condición de cruce hacia abajo: El centro estaba arriba de la línea y ahora está en la línea o debajo
        crossing_down = prev_cy < counting_line_y and curr_cy >= counting_line_y

        # Condición de cruce hacia arriba: El centro estaba debajo de la línea y ahora está en la línea o arriba
        crossing_up = prev_cy > counting_line_y and curr_cy <= counting_line_y

        if crossing_down or crossing_up:
            vehicle_counts[vehicle_type] += 1
            counted_ids.add(track_id)
            return True
    return False

