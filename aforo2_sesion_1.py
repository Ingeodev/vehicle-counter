from datetime import datetime, timedelta

class VehicleCounter:

    def __init__(self,
                 model_path,
                 mask_path=None,
                 zones_path=None,
                 reduce_factor=1,
                 device="cpu",
                 output_folder="./output",
                 classes={1: 'bicycle', 2: 'car', 3: 'motorcycle', 5: 'bus', 7: 'truck'},
                 max_minutes=None,
                 verbose=True
                ):
        """
        Inicializa el contador de vehículos.

        Args:
            model_path (str): Ruta al modelo YOLO
            mask_path (str, optional): Ruta a la máscara de región de interés
            zones_path (str, optional): Ruta al JSON con zonas de interés
            reduce_factor (int): Factor de reducción de resolución (1=original, 2=mitad, etc)
            device (str): Dispositivo para YOLO ('cpu', 'cuda', 'mps')
            output_folder (str): Carpeta donde guardar resultados
            max_minutes (int, optional): Límite de minutos a procesar
            verbose (bool): Si True, muestra mensajes de inicialización
        """

        self.verbose = verbose

        # Cargar modelo YOLO
        if self.verbose:
            print("🔄 Cargando modelo YOLO...")
        self.model = YOLO(model_path)
        self.device = device
        if self.verbose:
            print(f"✅ Modelo cargado en: {device}")

        # Configuración
        self.reduce_factor = reduce_factor
        self.output_folder = output_folder
        self.max_minutes = max_minutes
        os.makedirs(self.output_folder, exist_ok=True)

        # Clases de vehículos permitidas
