    def process_video(self, video_path, base_time, output_folder='/content/', output_name="result.mp4", progress_interval=100):
        """
        Procesa un video completo y genera el video con anotaciones.

        Args:
            video_path (str): Ruta al video de entrada
            base_time (str): Hora base del video en formato HH:MM o HH:MM:SS
            output_folder (str): Carpeta donde guardar el resultado
            output_name (str): Nombre del archivo de salida
            progress_interval (int): Cada cuántos frames mostrar progreso

        Returns:
            dict: Diccionario con estadísticas y ruta del CSV
        """

        # Abrir video
        cap = cv2.VideoCapture(video_path)
        if not cap.isOpened():
            raise ValueError(f"❌ No se pudo abrir el video: {video_path}")

        # Propiedades del video
        fps = cap.get(cv2.CAP_PROP_FPS)
        orig_w = int(cap.get(cv2.CAP_PROP_FRAME_WIDTH))
        orig_h = int(cap.get(cv2.CAP_PROP_FRAME_HEIGHT))
        total_frames = int(cap.get(cv2.CAP_PROP_FRAME_COUNT))

        # Tamaño de procesamiento
        w = orig_w // self.reduce_factor
        h = orig_h // self.reduce_factor

        # Escalar zonas según el factor de reducción
        if self.zones_original:
            self.scale_zones(orig_w, orig_h, w, h)

        # Redimensionar máscara si existe
        if self.mask is not None:
            self.mask = cv2.resize(self.mask, (w, h), interpolation=cv2.INTER_NEAREST)

        # Calcular frames a procesar
        if self.max_minutes is None or self.max_minutes <= 0:
            frames_to_process = total_frames
            mode_text = "VIDEO COMPLETO"
        else:
            limit_frames = int(self.max_minutes * 60 * fps)
            frames_to_process = min(total_frames, limit_frames)
            mode_text = f"{self.max_minutes} minutos"

        # Configurar salida
        output_path = os.path.join(output_folder, output_name)
        fourcc = cv2.VideoWriter_fourcc(*"mp4v")
        writer = cv2.VideoWriter(output_path, fourcc, fps, (w, h))

        # Variables de control
        frame_index = 0
        start_time = time.time()

        # Mensaje inicial
        if self.verbose:
            print("\n" + "="*70)
            print("🎬 PROCESANDO VIDEO")
            print("="*70)
            print(f"📹 Entrada: {video_path}")
            print(f"💾 Salida: {output_path}")
            print(f"🕐 Hora base: {base_time}")
            print(f"📊 Resolución: {orig_w}x{orig_h} → {w}x{h}")
            print(f"🎯 Frames a procesar: {frames_to_process} ({mode_text})")
            print(f"⚡ FPS: {fps:.1f}")
            print(f"🔷 Zonas de interés: {len(self.zones)}")
            print("="*70)
            print("")

        # Loop principal
        while True:
            ret, frame = cap.read()
            if not ret:
                break
            if frame_index >= frames_to_process:
                break

            # Reducir tamaño
            frame = cv2.resize(frame, (w, h))

            # Tiempo actual en segundos
            current_time = frame_index / fps

            # Detecciones YOLO
            results = self.model.track(
                frame,
                persist=True,
                device=self.device,
                classes=list(self.allowed_vehicle_classes.keys()),
                verbose=False
            )[0]

            # Dibujar zonas de interés
            if self.zones:
                self.draw_zones(frame, alpha=0.2)

            if results.boxes.id is not None:
                # Extraer detecciones
                boxes = results.boxes.xyxy.cpu().numpy()
                ids = results.boxes.id.cpu().numpy().astype(int)
                cls = results.boxes.cls.cpu().numpy().astype(int)

                detections = np.hstack([boxes, ids.reshape(-1, 1), cls.reshape(-1, 1)])

                # Filtrar detecciones
                detections = self.filter_detections(detections)

                # Procesar cada detección
                for det in detections:
                    x1, y1, x2, y2, track_id, cls_id = det
                    cx = int((x1 + x2) / 2)
                    cy = int((y1 + y2) / 2)

                    label = self.allowed_vehicle_classes.get(int(cls_id))

                    # Verificar entrada a zonas (ahora con base_time)
                    entered_zone = self.check_zone_entry(track_id, cx, cy, current_time, label, base_time)

                    # Color según si ya entró a alguna zona
                    has_entries = track_id in self.vehicle_zone_history and len(self.vehicle_zone_history[track_id]) > 0
                    color = (0, 255, 0) if has_entries else (255, 0, 0)

                    # Dibujar
                    cv2.circle(frame, (cx, cy), 5, (0, 255, 255), -1)
                    cv2.rectangle(frame, (int(x1), int(y1)), (int(x2), int(y2)), color, 2)

                    # Mostrar ID y tipo
                    text = f"{label}-{track_id}"
                    cv2.putText(frame, text, (int(x1), int(y1) - 5),
                               cv2.FONT_HERSHEY_SIMPLEX, 0.5, (255, 255, 255), 2)

                    # Mostrar zonas visitadas
                    if has_entries:
                        zones_visited = [e['zone'] for e in self.vehicle_zone_history[track_id]]
                        zones_text = f"Zonas: {','.join(zones_visited)}"
                        cv2.putText(frame, zones_text, (int(x1), int(y2) + 15),
                                   cv2.FONT_HERSHEY_SIMPLEX, 0.4, (0, 255, 0), 1)

                    self.prev_positions[track_id] = (cx, cy)

            # Mostrar estadísticas en el frame
            y_offset = 30
            cv2.putText(frame, f"Detecciones: {len(self.detection_log)}", (10, y_offset),
                       cv2.FONT_HERSHEY_SIMPLEX, 0.7, (255, 255, 255), 2)

            y_offset += 30
            cv2.putText(frame, f"Tiempo: {self.format_timestamp(current_time)}", (10, y_offset),
                       cv2.FONT_HERSHEY_SIMPLEX, 0.7, (255, 255, 255), 2)

            writer.write(frame)
            frame_index += 1

            # Mostrar progreso
            if frame_index % progress_interval == 0:
                elapsed_time = time.time() - start_time
                percent = (frame_index / frames_to_process) * 100
                fps_actual = frame_index / elapsed_time if elapsed_time > 0 else 0
                remaining = (frames_to_process - frame_index) / fps_actual if fps_actual > 0 else 0

                minutes = int(elapsed_time // 60)
                seconds = int(elapsed_time % 60)

                print(f"⏳ {frame_index}/{frames_to_process} frames | "
                      f"{percent:.1f}% | "
                      f"{fps_actual:.1f} FPS | "
                      f"Tiempo: {minutes:02d}:{seconds:02d} | "
                      f"Restante: ~{int(remaining)}s | "
                      f"Detecciones: {len(self.detection_log)}")

        # Liberar recursos
        cap.release()
        writer.release()

        # Guardar CSV
        csv_path = self.save_detection_log(output_path)

        # Mensaje final
        elapsed_total = time.time() - start_time
        if self.verbose:
            print("\n" + "="*70)
            print("✅ PROCESO COMPLETADO")
            print("="*70)
            print(f"⏱️ Tiempo total: {elapsed_total/60:.2f} minutos ({elapsed_total:.0f} segundos)")
            print(f"📁 Video guardado: {output_path}")
            print(f"📊 CSV guardado: {csv_path}")
            print(f"\n🚗 RESUMEN DE DETECCIONES:")
            print(f"   Total de entradas a zonas: {len(self.detection_log)}")

            # Resumen por zona
            zone_counts = defaultdict(int)
            for entry in self.detection_log:
                zone_counts[entry['zone']] += 1

            print(f"\n📍 DETECCIONES POR ZONA:")
            for zone, count in sorted(zone_counts.items()):
                print(f"   Zona {zone}: {count} entradas")

            print("="*70)

        return {
            'video_path': output_path,
            'csv_path': csv_path,
            'total_detections': len(self.detection_log),
            'zone_counts': dict(zone_counts),
            'processing_time': elapsed_total
        }

