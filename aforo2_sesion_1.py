    def set_zones(self, zones_path):
      try:
          with open(zones_path, 'r') as f:
              zones_data = json.load(f)
              self.zones_original = zones_data.get('zones', [])
              if self.verbose:
                  print(f"✅ Zonas cargadas: {len(self.zones_original)} zonas")
                  for zone in self.zones_original:
                      print(f"   - Zona {zone['label']}: {len(zone['points'])} puntos")
      except Exception as e:
          if self.verbose:
              print(f"⚠️ Error al cargar zonas: {e}")


    def scale_zones(self, original_width, original_height, new_width, new_height):
        """
        Escala las coordenadas de las zonas según la resolución de procesamiento.

        Args:
            original_width, original_height: Dimensiones originales del video/imagen
            new_width, new_height: Nuevas dimensiones de procesamiento
        """
        scale_x = new_width / original_width
        scale_y = new_height / original_height

        self.zones = []
        for zone in self.zones_original:
            scaled_points = []
            for point in zone['points']:
                scaled_x = int(point[0] * scale_x)
                scaled_y = int(point[1] * scale_y)
                scaled_points.append([scaled_x, scaled_y])

            self.zones.append({
                'label': zone['label'],
                'points': np.array(scaled_points, dtype=np.int32)
            })

        if self.verbose:
            print(f"🔧 Zonas escaladas de {original_width}x{original_height} a {new_width}x{new_height}")


    def point_in_zone(self, point, zone_points):
        """
        Verifica si un punto está dentro de un polígono (zona).

        Args:
            point: Tupla (x, y)
            zone_points: Array numpy con los puntos del polígono

        Returns:
            bool: True si el punto está dentro de la zona
        """
        return cv2.pointPolygonTest(zone_points, point, False) >= 0


    def calculate_exact_time(self, base_time, elapsed_seconds):
        """
        Calcula la hora exacta sumando el tiempo base del video más los segundos transcurridos.

        Soporta formato de hora base HH:MM o HH:MM:SS.

        Args:
            base_time (str): Hora base en formato HH:MM o HH:MM:SS
            elapsed_seconds (float): Segundos transcurridos desde el inicio del video

        Returns:
            str: Hora exacta en formato HH:MM:SS
        """

        # 1. Determinar el formato y parsear la hora base
        try:
            # Intentar parsear HH:MM:SS
            if len(base_time.split(':')) == 3:
                base_time_format = '%H:%M:%S'
            # Intentar parsear HH:MM
            elif len(base_time.split(':')) == 2:
                base_time_format = '%H:%M'
            else:
                raise ValueError("Formato de base_time inválido. Use HH:MM o HH:MM:SS.")

            # Usamos una fecha ficticia (2000, 1, 1) para manejar solo el tiempo
            base_datetime_str = f"2000-01-01 {base_time}"
            base_datetime = datetime.strptime(base_datetime_str, f"%Y-%m-%d {base_time_format}")

        except ValueError as e:
            if self.verbose:
                print(f"⚠️ Error al parsear base_time '{base_time}': {e}")
            # En caso de error de parseo, retorna None o la base original para evitar fallos.
            return None


        # 2. Sumar los segundos transcurridos
        exact_datetime = base_datetime + timedelta(seconds=elapsed_seconds)

        # 3. Retornar en formato HH:MM:SS
        return exact_datetime.strftime('%H:%M:%S')


    def check_zone_entry(self, track_id, cx, cy, current_time, vehicle_type, base_time=None):
        """
        Verifica si un vehículo entró a alguna zona nueva.

        Args:
            track_id: ID del vehículo
            cx, cy: Coordenadas del centro del vehículo
            current_time: Tiempo actual en segundos
            vehicle_type: Tipo de vehículo
            base_time: Hora base del video en formato HH:MM o HH:MM:SS

        Returns:
            str or None: Label de la zona si entró a una nueva, None en caso contrario
        """
        # Inicializar historial si es la primera vez
        if track_id not in self.vehicle_zone_history:
            self.vehicle_zone_history[track_id] = []

        # Verificar cada zona
        for zone in self.zones:
            zone_label = zone['label']

            # Verificar si está dentro de esta zona
            if self.point_in_zone((cx, cy), zone['points']):

                # Verificar si ya había entrado a esta zona
                already_in_zone = any(
                    entry['zone'] == zone_label
                    for entry in self.vehicle_zone_history[track_id]
                )

                if not already_in_zone:
                    # Calcular hora exacta si se proporciona base_time
                    exact_time = None
                    if base_time:
                        exact_time = self.calculate_exact_time(base_time, current_time)

                    # Nueva entrada a esta zona
                    entry = {
                        'zone': zone_label,
                        'timestamp': current_time,
                        'vehicle_type': vehicle_type
                    }
                    self.vehicle_zone_history[track_id].append(entry)

                    # Agregar al log de detecciones
                    log_entry = {
                        'vehicle_id': track_id,
                        'vehicle_type': vehicle_type,
                        'zone': zone_label,
                        'timestamp_seconds': current_time,
                        'timestamp_formatted': self.format_timestamp(current_time)
                    }

                    # Añadir hora exacta si está disponible
                    if exact_time:
                        log_entry['exact_time'] = exact_time

                    self.detection_log.append(log_entry)

                    return zone_label

        return None


    def format_timestamp(self, seconds):
        """Convierte segundos a formato MM:SS"""
        minutes = int(seconds // 60)
        secs = int(seconds % 60)
        return f"{minutes:02d}:{secs:02d}"


    def filter_detections(self, detections):
        """
        Filtra detecciones para mantener solo vehículos dentro de la máscara.

        Args:
            detections (np.array): Array de detecciones [x1, y1, x2, y2, track_id, cls_id]

        Returns:
            list: Lista de detecciones filtradas
        """
        filtered = []

        for det in detections:
            x1, y1, x2, y2, track_id, cls_id = det
            cls_id_int = int(cls_id)

            # Solo vehículos permitidos
            if cls_id_int not in self.allowed_vehicle_classes:
                continue

            # Centro del bounding box
            cx = int((x1 + x2) / 2)
            cy = int((y1 + y2) / 2)

            # Verificar máscara
            if self.mask is not None:
                if cy >= self.mask.shape[0] or cx >= self.mask.shape[1]:
                    continue
                if self.mask[cy, cx] == 0:
                    continue

            filtered.append(det)

        return filtered


    def draw_zones(self, frame, alpha=0.3):
        """
        Dibuja las zonas de interés en el frame.

        Args:
            frame: Frame de video
            alpha: Transparencia del relleno (0-1)
        """
        overlay = frame.copy()

        # Colores diferentes para cada zona
        colors = [
            (255, 0, 0), (0, 255, 0), (0, 0, 255), (255, 255, 0),
            (255, 0, 255), (0, 255, 255), (128, 0, 128), (255, 128, 0),
            (128, 255, 0)
        ]

        for i, zone in enumerate(self.zones):
            color = colors[i % len(colors)]

            # Dibujar polígono relleno
            cv2.fillPoly(overlay, [zone['points']], color)

            # Dibujar contorno
            cv2.polylines(frame, [zone['points']], True, color, 2)

            # Etiqueta de la zona
            centroid = zone['points'].mean(axis=0).astype(int)
            cv2.putText(frame, zone['label'], tuple(centroid),
                       cv2.FONT_HERSHEY_SIMPLEX, 1.0, (255, 255, 255), 2)

        # Mezclar overlay con frame original
        cv2.addWeighted(overlay, alpha, frame, 1 - alpha, 0, frame)


