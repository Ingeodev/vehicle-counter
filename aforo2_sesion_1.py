def create_directory_tree(path):
    tree = {}

    try:
        # Itera sobre el contenido de la ruta actual
        for item_name in os.listdir(path):
            item_path = os.path.join(path, item_name)

            # Si es un directorio, llama a la función recursivamente
            if os.path.isdir(item_path):
                # La clave es el nombre de la carpeta, el valor es el resultado de la llamada recursiva
                tree[item_name] = {
                        'type': 'folder',
                        'folder': create_directory_tree(item_path)
                    }

            # Si es un archivo, clasifícalo por extensión
            elif os.path.isfile(item_path):
                extension = os.path.splitext(item_name)[1].lower()
                if extension == '.mp4':
                    tree[item_name] = {
                        'type': 'video',
                        'path': item_path,
                        'hora_inicial': item_name.split('_')[1]
                    }
                elif extension == '.png':
                    tree[item_name] = {
                        'type': 'mask',
                        'path': item_path
                    }
                elif extension == '.json':
                    tree[item_name] = {
                        'type': 'zonas_interes',
                        'path': item_path
                    }
                else:
                    tree[item_name] = {
                        'type': 'unknown',
                        'path': item_path
                    } # Marcar como archivo genérico si no coincide

    except FileNotFoundError:
        print(f"Alerta: Ruta no encontrada o acceso denegado: {path}")
        return {} # Devuelve un diccionario vacío en caso de error
    except Exception as e:
        print(f"Error al procesar la ruta {path}: {e}")
        return {}

    return tree

